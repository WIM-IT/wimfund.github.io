</div><div id="wb_element_instance111" class="wb_element wb_text_element" style=" line-height: normal;"><p class="wb-stl-normal">    宏观面，10月工业企业利润5480亿元，同比增长3.6%，较上月小幅下降0.5个百分点。2018年1-10月份工业企业主营业务收入85.7万亿元，同比增速9.3%，较上月回落0.1%；利润5.52万亿元，累计同比增速13.6%，下行0.9%；经PPI调整后，实际主营业务收入累计同比增速为6%，上升0.6%。总的来看，10月工业企业利润增速延续之前下行趋势，下滑速度放缓。从生产经营角度看，工业企业的周转明显放缓，存货周转天数及应收账款天数持续上升。另一方面，企业的ROA和ROE持续下降，资产负债率回升。成本依然保持下行趋势。从行业来看，偏上游行业盈利仍好于中游制造业行业。2018年四季度尚未出现支撑工业企业利润向上的因素，预计11月及12月工业企业利润增速仍然维持在低位区间。</p>

<p class="wb-stl-normal">  10月通胀温和，CPI同比持平2.5%，食品和能源是主要支撑，但二者均有转弱，预示CPI年内高点已经出现，未来两月有望下行。PPI下游需求仍未见明显起色，叠加高基数，PPI 同比继续降至3.3%；环比角度，国内需求仍没有明显起色，带动整体环比小幅下行。贸易数据方面，10月进出口增速均大超预期，以美元计出口同比增长15.6%，超过预期11.7%及前值14.5%；进口同比增长21.4%，同样超过预期14.5%及前值14.3%。进出口增速走高，主要是受进出口贸易行为前移、汇率贬值，及去年工作日比今年少1天和环保限产导致数据基数偏低等影响。</p>

<p class="wb-stl-normal">  消息方面，银监会初步考虑对民营企业的贷款要实现“一二五”的目标。我们判断将导致新增信贷规模的快速扩张，最终提高社会融资规模的增速。另外一种可能情形就是一种逆向选择，银行将削减国企的新增贷款规模，令社会融资规模增速出现下滑。汽车行业10月车市销量同比下滑13.2% ，预计2019年全年增速为1.2%，或将呈前低后稳的走势。10月中国挖掘机销量增速意外大幅回升，同比增长44.9%，有低基数因素，也可能在于国内基建投资增速在10月有所企稳。</p>
